fun main() {
  val integer = 15
  val maxIntegerValue: Int = Int.MAX_VALUE
  val minIntegerValue: Int = Int.MIN_VALUE

  val myMaxByteValue: Byte = Byte.MAX_VALUE
  val myMinByteValue: Byte = Byte.MIN_VALUE

  val myMaxShortValue: Short = Short.MAX_VALUE
  val myMinShortValue: Short = Short.MIN_VALUE

  println("Integer: $integer,\nMaximum Integer Value: $maxIntegerValue,\nMinimum Integer Value: $minIntegerValue")
  println("\nMaximum Byte Value: $myMaxByteValue,\nMinimum Byte Value: $myMinByteValue")
  println("\nMaximum Short Value: $myMaxShortValue,\nMinimum Short Value: $myMinShortValue")
}